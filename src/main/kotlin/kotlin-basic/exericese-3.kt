package `kotlin-basic`

fun main(args: Array<String>){

    val length = 4
    val width = 5
    // calculating Rectangular Area
    val area = length * width
    println("Area of rectangular : " + area)

    // Calculating Rectangle Perimeter
    val perimeter = 2 * (length + width)
    println("Perimeter of rectangle : " + perimeter)

}

